/**
 * Generate HTML wrapper
 */
function wrapHtml(title: string, content: string): string {
  return `<!DOCTYPE html>
<html lang="en">
<head>
  <meta charset="UTF-8">
  <meta name="viewport" content="width=device-width, initial-scale=1.0">
  <title>${escapeHtml(title)}</title>
  ${STYLES}
</head>
<body>
  ${content}
  <footer>
    Generated by PopKit Benchmark Framework
  </footer>
</body>
</html>`;
}

/**
 * Escape HTML entities
 */
function escapeHtml(str: string): string {
  return str
    .replace(/&/g, '&amp;')
    .replace(/</g, '&lt;')
    .replace(/>/g, '&gt;')
    .replace(/"/g, '&quot;')
    .replace(/'/g, '&#039;');
}

/**
 * Generate progress bar HTML
 */
function progressBarHtml(value: number, max: number, label?: string): string {
  const percentage = Math.min((value / max) * 100, 100);
  return `
    <div class="progress-bar">
      <div class="progress-fill" style="width: ${percentage}%"></div>
      ${label ? `<span class="progress-label">${escapeHtml(label)}</span>` : ''}
    </div>
  `;
}

/**
 * Generate a single result report in HTML
 */
export function generateResultHtml(result: BenchmarkResult): string {
  const content = `
    <h1>Benchmark Result: ${escapeHtml(result.taskId)}</h1>

    <div class="card">
      <h2>Overview</h2>
      <div class="summary-grid">
        <div class="metric-card">
          <div class="metric-value">${result.success ? '✓' : '✗'}</div>
          <div class="metric-label">${result.success ? 'Passed' : 'Failed'}</div>
        </div>
        <div class="metric-card">
          <div class="metric-value">${formatNumber(result.tokensUsed)}</div>
          <div class="metric-label">Tokens Used</div>
        </div>
        <div class="metric-card">
          <div class="metric-value">${result.toolCalls}</div>
          <div class="metric-label">Tool Calls</div>
        </div>
        <div class="metric-card">
          <div class="metric-value">${formatDuration(result.durationSeconds)}</div>
          <div class="metric-label">Duration</div>
        </div>
      </div>

      <table>
        <tr><th>Property</th><th>Value</th></tr>
        <tr><td>Run ID</td><td><code>${result.runId}</code></td></tr>
        <tr><td>Tool</td><td>${escapeHtml(result.tool)}</td></tr>
        <tr><td>Mode</td><td>${MODE_NAMES[result.mode]}</td></tr>
        <tr><td>Started</td><td>${new Date(result.startedAt).toLocaleString()}</td></tr>
        <tr><td>Quality Score</td><td>${result.codeQualityScore.toFixed(1)}/10</td></tr>
      </table>
    </div>

    ${result.testResults.length > 0 ? `
    <div class="card">
      <h2>Test Results</h2>
      <table>
        <thead>
          <tr><th>Test</th><th>Status</th><th>Duration</th></tr>
        </thead>
        <tbody>
          ${result.testResults.map(test => `
            <tr>
              <td>${escapeHtml(test.name)}</td>
              <td><span class="badge ${test.passed ? 'badge-success' : 'badge-error'}">${test.passed ? 'Pass' : 'Fail'}</span></td>
              <td>${test.durationMs}ms</td>
            </tr>
          `).join('')}
        </tbody>
      </table>
    </div>
    ` : ''}
  `;

  return wrapHtml(`Benchmark: ${result.taskId}`, content);
}

/**
 * Generate comparison report in HTML
 */
export function generateComparisonHtml(report: ComparisonReport): string {
  const content = `
    <h1>Benchmark Comparison: ${escapeHtml(report.taskId)}</h1>

    <div class="card">
      <h2>Summary</h2>
      <div class="summary-grid">
        <div class="metric-card">
          <div class="metric-value">${MODE_NAMES[report.summary.bestMode]}</div>
          <div class="metric-label">Best Mode</div>
        </div>
        <div class="metric-card">
          <div class="metric-value">${report.summary.tokenSavingsPercent.toFixed(1)}%</div>
          <div class="metric-label">Token Savings</div>
        </div>
        <div class="metric-card">
          <div class="metric-value">+${report.summary.successRateImprovement.toFixed(1)}</div>
          <div class="metric-label">Success Rate (pp)</div>
        </div>
        <div class="metric-card">
          <div class="metric-value">+${report.summary.qualityImprovement.toFixed(1)}</div>
          <div class="metric-label">Quality (pts)</div>
        </div>
      </div>
    </div>

    <div class="card">
      <h2>Mode Comparison</h2>
      ${generateComparisonVisual(report)}
    </div>
  `;

  return wrapHtml(`Comparison: ${report.taskId}`, content);
}

/**
 * Generate visual comparison bars
 */
function generateComparisonVisual(report: ComparisonReport): string {
  const modes = ['vanilla', 'popkit', 'power'] as const;
  // Placeholder values - would be populated from actual comparison data
  const tokenData = { vanilla: 100, popkit: 63, power: 49 };
  const successData = { vanilla: 48, popkit: 84, power: 96 };

  return `
    <h3>Token Usage</h3>
    ${modes.map(mode => `
      <div class="comparison-row">
        <div class="comparison-label">${MODE_NAMES[mode]}</div>
        <div class="comparison-bar">
          ${progressBarHtml(tokenData[mode], 100)}
        </div>
        <div class="comparison-value">${tokenData[mode]}%</div>
      </div>
    `).join('')}

    <h3>Success Rate</h3>
    ${modes.map(mode => `
      <div class="comparison-row">
        <div class="comparison-label">${MODE_NAMES[mode]}</div>
        <div class="comparison-bar">
          ${progressBarHtml(successData[mode], 100)}
        </div>
        <div class="comparison-value">${successData[mode]}%</div>
      </div>
    `).join('')}
  `;
}

/**
 * Generate aggregate report in HTML
 */
export function generateAggregateHtml(report: AggregateReport): string {
  const content = `
    <h1>${escapeHtml(report.title)}</h1>

    <div class="card">
      <h2>Executive Summary</h2>
      <div class="summary-grid">
        <div class="metric-card">
          <div class="metric-value">${report.taskCount}</div>
          <div class="metric-label">Tasks Benchmarked</div>
        </div>
        <div class="metric-card">
          <div class="metric-value">${report.runCount}</div>
          <div class="metric-label">Total Runs</div>
        </div>
        <div class="metric-card">
          <div class="metric-value">${report.averages.tokenReduction.toFixed(1)}%</div>
          <div class="metric-label">Avg Token Reduction</div>
        </div>
        <div class="metric-card">
          <div class="metric-value">+${report.averages.successRateImprovement.toFixed(1)}pp</div>
          <div class="metric-label">Avg Success Improvement</div>
        </div>
      </div>
    </div>

    <div class="card">
      <h2>Per-Task Results</h2>
      <table>
        <thead>
          <tr>
            <th>Task</th>
            <th>Best Mode</th>
            <th>Token Savings</th>
            <th>Success Improvement</th>
          </tr>
        </thead>
        <tbody>
          ${report.tasks.map(task => `
            <tr>
              <td>${escapeHtml(task.taskName)}</td>
              <td><span class="badge badge-info">${MODE_NAMES[task.comparison.summary.bestMode]}</span></td>
              <td>${task.comparison.summary.tokenSavingsPercent.toFixed(1)}%</td>
              <td>+${task.comparison.summary.successRateImprovement.toFixed(1)} pp</td>
            </tr>
          `).join('')}
        </tbody>
      </table>
    </div>

    <div class="card">
      <h2>Statistical Confidence</h2>
      <p>
        <span class="badge ${report.confidence.sufficientSamples ? 'badge-success' : 'badge-error'}">
          ${report.confidence.sufficientSamples ? 'Sufficient Samples' : 'Insufficient Samples'}
        </span>
      </p>
      <p>
        ${report.confidence.sufficientSamples
          ? 'Sample size is sufficient for statistical significance.'
          : 'More benchmark runs are recommended for statistical significance.'}
      </p>
    </div>
  `;

  return wrapHtml(report.title, content);
}
