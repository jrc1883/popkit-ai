class TestCursorInstallUninstall:
    """Tests for Cursor install/uninstall operations."""

    def test_install_creates_mcp_json(self, tmp_path):
        """Install creates ~/.cursor/mcp.json with popkit entry."""
        adapter = CursorAdapter()
        package_dir = tmp_path / "popkit-core"
        package_dir.mkdir()

        with (
            patch.object(Path, "home", return_value=tmp_path),
            patch.dict(os.environ, {"POPKIT_HOME": str(tmp_path)}),
        ):
            result = adapter.install(package_dir)

        assert result is True
        mcp_json = tmp_path / ".cursor" / "mcp.json"
        assert mcp_json.is_file()

        with open(mcp_json, encoding="utf-8") as f:
            config = json.load(f)

        assert "mcpServers" in config
        assert "popkit" in config["mcpServers"]

    def test_install_merges_with_existing(self, tmp_path):
        """Install preserves existing MCP server entries."""
        adapter = CursorAdapter()
        package_dir = tmp_path / "popkit-core"
        package_dir.mkdir()

        # Create existing mcp.json with another server
        cursor_dir = tmp_path / ".cursor"
        cursor_dir.mkdir()
        mcp_json = cursor_dir / "mcp.json"
        existing = {
            "mcpServers": {
                "other-server": {
                    "command": "node",
                    "args": ["server.js"],
                }
            }
        }
        with open(mcp_json, "w", encoding="utf-8") as f:
            json.dump(existing, f)

        with (
            patch.object(Path, "home", return_value=tmp_path),
            patch.dict(os.environ, {"POPKIT_HOME": str(tmp_path)}),
        ):
            result = adapter.install(package_dir)

        assert result is True

        with open(mcp_json, encoding="utf-8") as f:
            config = json.load(f)

        # Both entries should exist
        assert "other-server" in config["mcpServers"]
        assert "popkit" in config["mcpServers"]

    def test_install_handles_malformed_existing_json(self, tmp_path):
        """Install handles corrupted mcp.json gracefully."""
        adapter = CursorAdapter()
        package_dir = tmp_path / "popkit-core"
        package_dir.mkdir()

        cursor_dir = tmp_path / ".cursor"
        cursor_dir.mkdir()
        mcp_json = cursor_dir / "mcp.json"
        mcp_json.write_text("{invalid json", encoding="utf-8")

        with (
            patch.object(Path, "home", return_value=tmp_path),
            patch.dict(os.environ, {"POPKIT_HOME": str(tmp_path)}),
        ):
            result = adapter.install(package_dir)

        assert result is True

        with open(mcp_json, encoding="utf-8") as f:
            config = json.load(f)

        assert "popkit" in config["mcpServers"]

    def test_uninstall_removes_popkit_entry(self, tmp_path):
        """Uninstall removes popkit from mcp.json."""
        adapter = CursorAdapter()

        cursor_dir = tmp_path / ".cursor"
        cursor_dir.mkdir()
        mcp_json = cursor_dir / "mcp.json"
        config = {
            "mcpServers": {
                "popkit": {"command": "python3", "args": ["-m", "popkit_mcp.server"]},
                "other-server": {"command": "node", "args": ["server.js"]},
            }
        }
        with open(mcp_json, "w", encoding="utf-8") as f:
            json.dump(config, f)

        with patch.object(Path, "home", return_value=tmp_path):
            result = adapter.uninstall("popkit")

        assert result is True

        with open(mcp_json, encoding="utf-8") as f:
            config = json.load(f)

        assert "popkit" not in config["mcpServers"]
        assert "other-server" in config["mcpServers"]

    def test_uninstall_no_mcp_json(self, tmp_path):
        """Uninstall returns False when no mcp.json exists."""
        adapter = CursorAdapter()

        with patch.object(Path, "home", return_value=tmp_path):
            result = adapter.uninstall("popkit")

        assert result is False

    def test_uninstall_no_popkit_entry(self, tmp_path):
        """Uninstall returns False when popkit is not in mcp.json."""
        adapter = CursorAdapter()

        cursor_dir = tmp_path / ".cursor"
        cursor_dir.mkdir()
        mcp_json = cursor_dir / "mcp.json"
        with open(mcp_json, "w", encoding="utf-8") as f:
            json.dump({"mcpServers": {}}, f)

        with patch.object(Path, "home", return_value=tmp_path):
            result = adapter.uninstall("popkit")

        assert result is False


