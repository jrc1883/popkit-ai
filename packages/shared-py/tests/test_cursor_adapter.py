# Detection Tests
# =============================================================================


class TestCursorDetection:
    """Tests for Cursor detection logic."""

    def test_name_and_display_name(self):
        adapter = CursorAdapter()
        assert adapter.name == "cursor"
        assert adapter.display_name == "Cursor"

    def test_detect_returns_provider_info(self):
        adapter = CursorAdapter()
        info = adapter.detect()
        assert isinstance(info, ProviderInfo)
        assert info.name == "cursor"

    def test_detect_with_home_dir(self, tmp_path):
        """Detected as available when ~/.cursor/ exists."""
        adapter = CursorAdapter()
        cursor_dir = tmp_path / ".cursor"
        cursor_dir.mkdir()

        with patch.object(Path, "home", return_value=tmp_path):
            info = adapter.detect()
            assert info.is_available is True
            assert info.install_path == cursor_dir

    def test_detect_without_cursor(self, tmp_path):
        """Not available when no .cursor/ dir and no binary."""
        adapter = CursorAdapter()

        with (
            patch.object(Path, "home", return_value=tmp_path),
            patch("popkit_shared.providers.cursor.shutil.which", return_value=None),
            patch("popkit_shared.providers.cursor.Path.is_dir", return_value=False),
        ):
            # Patch Path(".cursor").is_dir() and Path.home()/.cursor to not exist
            info = adapter.detect()
            assert info.is_available is False
            assert info.install_path is None

    def test_detect_with_binary_on_path(self, tmp_path):
        """Detected as available when cursor binary is on PATH."""
        adapter = CursorAdapter()

        with (
            patch.object(Path, "home", return_value=tmp_path),
            patch("popkit_shared.providers.cursor.shutil.which", return_value="/usr/bin/cursor"),
        ):
            info = adapter.detect()
            assert info.is_available is True


