class TestCursorConfigGeneration:
    """Tests for Cursor config generation."""

    def test_generate_config_creates_mcp_json(self, tmp_path):
        """Generate config creates a valid mcp-config.json."""
        adapter = CursorAdapter()
        package_dir = tmp_path / "popkit-core"
        package_dir.mkdir()
        output_dir = tmp_path / "output"

        with patch.dict(os.environ, {"POPKIT_HOME": str(tmp_path)}):
            result = adapter.generate_config(package_dir, output_dir)

        assert len(result) >= 1
        config_path = result[0]
        assert config_path.name == "mcp-config.json"
        assert config_path.is_file()

        with open(config_path, encoding="utf-8") as f:
            config = json.load(f)

        assert "mcpServers" in config
        assert "popkit" in config["mcpServers"]
        server = config["mcpServers"]["popkit"]
        assert server["command"] == "python3"
        assert server["args"] == ["-m", "popkit_mcp.server"]
        assert "POPKIT_HOME" in server["env"]

    def test_generate_config_creates_cursorrules(self, tmp_path):
        """Generate config creates .cursorrules from AGENT.md files."""
        adapter = CursorAdapter()
        package_dir = tmp_path / "popkit-core"
        agents_dir = package_dir / "agents" / "researcher"
        agents_dir.mkdir(parents=True)

        agent_md = agents_dir / "AGENT.md"
        agent_md.write_text("You are a research agent.\n\nDo research tasks.", encoding="utf-8")

        output_dir = tmp_path / "output"

        with patch.dict(os.environ, {"POPKIT_HOME": str(tmp_path)}):
            result = adapter.generate_config(package_dir, output_dir)

        # Should have mcp-config.json + at least one .cursorrules file
        assert len(result) >= 2
        rules_files = [p for p in result if ".cursorrules" in p.name]
        assert len(rules_files) == 1

        content = rules_files[0].read_text(encoding="utf-8")
        assert "Generated by PopKit" in content
        assert "You are a research agent." in content

    def test_generate_config_no_agent_md(self, tmp_path):
        """Generate config with no AGENT.md files produces only mcp-config.json."""
        adapter = CursorAdapter()
        package_dir = tmp_path / "popkit-core"
        package_dir.mkdir()
        output_dir = tmp_path / "output"

        with patch.dict(os.environ, {"POPKIT_HOME": str(tmp_path)}):
            result = adapter.generate_config(package_dir, output_dir)

        assert len(result) == 1
        assert result[0].name == "mcp-config.json"


